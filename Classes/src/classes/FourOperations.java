package classes;

public class FourOperations {

	public int sum(int num1,int num2)
	{
		return num1+num2;
	}
	
	public int product(int num1,int num2)
	{
		return num1*num2;
	}
	
	public int divide(int num1,int num2)
	{
		return num1/num2;
	}
	
	public int difference(int num1,int num2)
	{
		return num1-num2;
	}

}
