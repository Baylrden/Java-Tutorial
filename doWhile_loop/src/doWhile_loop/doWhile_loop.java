package doWhile_loop;

public class doWhile_loop {

	public static void main(String[] args) {
		int i=0;
		
		do {
			
			System.out.println(i);
			i++;
		}
		
		while(i<=10);
		//difference with while: if condition does not supported,do while loop will be executed at least one time.

	}

}
